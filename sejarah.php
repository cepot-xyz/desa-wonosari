                        <h2>Perjalanan Sejarah Desa Wonosari</h2>
                        
                        <div style="margin-top:5vh;" class="sejarah-paragraph">
                            <h3>Asal Usul dan Latar Belakang</h3>
                            <p>Desa wonosari merupakan salah satu desa yang terletak di kec pegandon Kab Kendal. Desa wonosari telah ada sejak jaman kolonial Belanda. Sebelum tahun 1938 desa wonosari terbagi menjadi 3 desa yaitu desa Mbelang dan Tanjung. Desa Mbelang dibagi 2 dusun yaitu Mbelang dan Mijen, sedangkan desa Tanjung dibagi menjadi 3 dusun yaitu Pidik, Tanjung, dan Tegalsari. Kedua desa tersebut berbatasan didaerah Jatilawang.</p>
                        </div>

                        <div style="margin-top:5vh;" class="sejarah-paragraph">
                            <h3>Era Pemerintahan Kolonial</h3>
                            <p>Desa Tanjung pada saat itu dikepalai oleh kepala desa yang bernama bapak Sul/Serpin dan cariknya bernama Surjo. Sedangkan desa Mbelang dipimpin oleh H.Nurmin. pada saat itu wilayah ini telah dikuasai oleh Belanda, ini terbukti adanya peninggalan belanda yang berupa jembatan kereta api yang terletak didusun Pidik; yang mempunyai fungsi sebagai sarana transportasi angkutan kayu dari hutan. Pembuatan jembatan tersebut tidak melibatkan masyarakat desa Tanjung tetapi para pekerja diambil dari daerah lain seperti Semarang dan Kedu.</p>
                        </div>

                        <div style="margin-top:5vh;" class="sejarah-paragraph">
                            <h3>Penyatuan Desa Wonosari (1938)</h3>
                            <p>Menjelang masa berakhirnya kolonial Belanda tepatnya pada tahun 1938 desa Tanjung dan Mbelang dilebur menjadi satu desa yaitu desa Wonosari; yang terdiri dari 5 dusun; yaitu Mijen, Krajan, Tanjung, Tegalsari, dan Pidik. Tidak diketahui apa yang melatarbelakangi bersatunya dua desa tersebut. Yang menjabat kepala desa pertama kali di desa Wonosari adalah Pawiro, dan cariknya adalah Mugiyo.</p>
                        </div>

                        <div style="margin-top:5vh;" class="sejarah-paragraph">
                            <h3>Kepemimpinan Desa (1944-1961)</h3>
                            <p>Pada tahun 1944 masa jabatan Pawiro sebagai kepala desa telah habis dan digantikan oleh cariknya yang bernama Mugiyo. Dikarenakan Mugiyo menjadi kepala desa maka jabatan carik kosong. Setelah itu diangkatlah Tomo sebagai carik yang menggantikan Mugiyo. Setelah 16 tahun menjadi kepala desa pada tahun 1961 masa jabatan Mugiyo sebagai kepala desa telah habis, dan digantikan oleh cariknya yang bernama Hutomo. Setelah Hutomo diangkat menjadi kepala desa maka jabatan carik kosong, dan digantikan oleh Chaerudin.</p>
                        </div>

                        <div style="margin-top:5vh;" class="sejarah-paragraph">
                            <h3>Masa Kepemimpinan Tomo dan Parman (1961-1997)</h3>
                            <p>Beliau menjabat sebagai carik kurang lebih selama 27 tahun, dari tahun 1960 sampai 1987. Tomo menjabat sebagai kepala desa selama 15 tahun. Pada tahun 1976 masa jabatan Tomo telah habis dan digantikan oleh Parman. Beliau menjabat dari 1976 sampai 1997 (21 Th).</p>
                            <p>Pada masa pemerintahannya yang menjadi carik tetap Chaerudin sampai tahun 1987. Kemudian pada tahun 1987 masa jabatan Chaerudin sebagai carik telah habis. Selama 2 tahun dari tahun 1987 sampai 1989 jabatan carik kosong, barulah pada tahun 1990 terpilihlah carik baru yaitu Mohammad Ali Sodiq, yang menjabat sampai tahun 2017.</p>
                        </div>

                        <div style="margin-top:5vh;" class="sejarah-paragraph">
                            <h3>Era Reformasi dan Modern (1997-Sekarang)</h3>
                            <p>Pada tahun tersebut pula terdapat kebijakan bupati Kendal terkait ASN yang berada di Desa diseluruh kabupaten ditarik kembali untuk ditempatkan pada OPD se kabupaten kendal, sehingga desa melakukan penjaringan perngkat untuk mengisi kekosongan jabatan struktural yang ada dan hasil CAT tahun 2017 terpilih Sdr Abdillah sebagai Sekretaris desa sampai sekarang.</p>
                            <p>Kemudian posisi jabatan kepala desa Pada tahun 1997 masa jabatan Suparman sebagai Kepala Desa telah habis, sehingga perlu diadakan pemilihan kepala desa dan terpilihlah Sucipto sebagai kepala desa hingga tahun 2007, Pada Tahun 2007 diadakan pemilihan kepala desa dan terpilihlah Slamet sebagai kepala desa hingga tahun 2013, pada tahun 2013 diadakan pemilihan kepala desa dan terpilihlah Mukalil kemudian di tahun 2020 bapak mukalil maju kembali sebagai Kepala Desa Wonosari hingga sekarang.</p>
                        </div>
